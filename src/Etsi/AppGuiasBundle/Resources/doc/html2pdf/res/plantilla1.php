o superior a 5 sobre 10. <br><br>
La parte práctica de la asignatura incluirá la asistencia y realización de prácticas y visitas de 
campo, y la entrega de informes de actividades prácticas y trabajos dirigidos, en su caso. Como 
resultado de la valoración de asistencias e informes, cada alumno/a tendrá una nota de prácticas 
(entre 0 y 10). No es necesario obtener una nota mínima en esta parte práctica para optar a 
superar la asignatura. <br><br>
Si no se obtiene al menos un 5 en el examen teórico de la asignatura, la nota obtenida en éste 
será la calificación final del alumno. En caso de superar esa nota, la calificación final de la 
asignatura se compondrá en un 80% de la nota del examen teórico y en un 20% de la nota de la 
